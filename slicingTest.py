l = [10, 20, 30, 40, 50, 60]
print(l[2:])  # [30, 40, 50, 60]
print(l[:2])  # [10, 20]
print(l[1::2])
print(l[4:1:-2])

l = list(range(10))
l[2:5] = [20, 30]
print(l)  # [0, 1, 20, 30, 5, 6, 7, 8, 9]

del l[5:7]  # [0, 1, 20, 30, 5, 8, 9]
print(l)

l[3::2] = [11, 22]
print(l)  # [0, 1, 20, 11, 5, 22, 9]
